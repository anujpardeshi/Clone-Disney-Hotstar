let currentIndex = 0;
let myBox = document.getElementById("slider")

function displaySlide(slide_list, currentIndex) {
    myBox.innerHTML= "";
    let el = slide_list[currentIndex];
    let div = document.createElement("div");
    div.className ="slide"
    let img = document.createElement("img");
    img.src = el.img;
    div.append(img);
    myBox.append(div);
}

displaySlide(slide_list, currentIndex);

setInterval(function(){
    currentIndex = (currentIndex + 1) % slide_list.length;
    displaySlide(slide_list, currentIndex);
}, 300000)

